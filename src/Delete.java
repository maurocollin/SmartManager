    public Delete(int adminId, String adminName) {
        initComponents();
        setIcon();

        this.adminId = adminId;
        this.adminName = adminName;
        List<String> cadena = object.allContacts(adminId);

        DefaultTableModel model = (DefaultTableModel) tblConsultDelete.getModel();

        model.setRowCount(0);

        for (String contactDetails : cadena) {
            String[] rowData = contactDetails.split("\n");
            model.addRow(rowData);
        }

        tblConsultDelete.setModel(model);
        jScrollPane1.setViewportView(tblConsultDelete);

        tblConsultDelete.setRowHeight(40);
    }

