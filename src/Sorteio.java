    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            String name = txtNome.getText();
            String lastName = txtSobrenome.getText();
            String phone_number = txtTelefone.getText();
            String email = txtEmail.getText();
            String brinde = txtBrinde.getText();
            String nunsort = numeroSorteado.getText();

            if (!name.isEmpty() && !lastName.isEmpty() && !phone_number.isEmpty() && !email.isEmpty() && !brinde.isEmpty() && !nunsort.isEmpty()) {
                MethodsUser object1 = new MethodsUser();
                object1.updateVencedor(name, lastName, phone_number, email, brinde, nunsort, adminId);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed
