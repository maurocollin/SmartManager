// SORTEIO RANDOMICO
    private void btnsortearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsortearActionPerformed
        Random aleatorio = new Random();
        numeros = Integer.parseInt(totaldeparticipantes.getText());
        int s = aleatorio.nextInt(this.numeros);
        numeroSorteado.setText(Integer.toString(s + 1));
// FUNCAO REVELAR GANHADOR
        try {
            MethodsUser object = new MethodsUser();
            id = Integer.parseInt(numeroSorteado.getText());
            List<String> updateDetails = object.getUpdate(id, adminId);

            if (updateDetails.size() >= 4) {
                numeroSorteado.setEnabled(true);
                txtNome.setText(updateDetails.get(0));
                txtSobrenome.setText(updateDetails.get(1));
                txtTelefone.setText(updateDetails.get(3));
                txtEmail.setText(updateDetails.get(2));

                txtNome.setEnabled(true);
                txtSobrenome.setEnabled(true);
                txtTelefone.setEnabled(true);
                txtEmail.setEnabled(true);
                btnSalvar.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "A lista não tem elementos suficientes");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Insira um ID válido");
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro: " + e.getMessage());
        }
// FIM FUNCAO REVELAR GANHADOR                    
    }//GEN-LAST:event_btnsortearActionPerformed

