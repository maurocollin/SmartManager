    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        MethodsUser users = new MethodsUser();
        String idParaBuscar = "123";
        int total = users.totalID(idParaBuscar);
        totaldeparticipantes.setText(String.valueOf(total));
// ALIMENTA A COMBOBOX COM OS DADOS DO BANCO DE BRINDES
        Connection con = null; // Fecha a conexão da combobo
            
        try {  //Abre a conexao
            Class.forName("org.mariadb.jdbc.Driver");
            con = DriverManager.getConnection(URL, USER, PASSWORD);
            System.out.println("Connection successful!");
            Statement stm = con.createStatement();
            ResultSet rs = stm.executeQuery("select name FROM produtos");
            
            while (rs.next()) {
                String name = rs.getString("name");
                jComboBox1.addItem(name);
            }
            con.close(); // Fecha a conexão da combobox
        } catch (ClassNotFoundException | SQLException e) {
            System.out.println("Connection failed: " + e.getMessage());
        }
    }//GEN-LAST:event_formWindowOpened
