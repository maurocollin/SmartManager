    public SelectAll(int adminId, String adminName) {
        initComponents(); // Inicializaci√≥n de componentes

        this.adminId = adminId;
        this.adminName=adminName;
        MethodsUser object = new MethodsUser();
        List<String> cadena = object.allContacts(adminId);

        DefaultTableModel model = (DefaultTableModel) tblUsers.getModel();

        // Limpiar la tabla antes de agregar nuevos datos
        model.setRowCount(0);

        // Agregar filas con los datos de la lista a la tabla
        for (String contactDetails : cadena) {
            String[] rowData = contactDetails.split("\n");
            model.addRow(rowData);
        }

        // Actualizar la tabla dentro del JScrollPane
        tblUsers.setModel(model);
        srlPanel.setViewportView(tblUsers);
        
        tblUsers.setRowHeight(40);
    }

